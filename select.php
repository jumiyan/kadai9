    $error = $stmt->errorInfo();
    exit('SQLError:' . print_r($error, true));
} else {
    while ($result = $stmt->fetch(PDO::FETCH_ASSOC)) {
        //GETデータ送信リンク作成
        // <a>で囲う。
        $view .= '<p>';
        $view .= '<a href="detail.php?id=' . $result['id'] . '">';
        $view .= $result['date'] . '：' . $result['name'];
        $view .= '</a>';
        $view .= '<a href="delete.php?id=' . $result['id'] . '">'; //追記
        $view .= '  [削除]'; //追記
        $view .= '</a>'; //追記
